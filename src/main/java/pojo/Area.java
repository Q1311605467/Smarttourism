package pojo;

public class Area {
    private int area_id;
    private String area_name;
    private String tourist_route;
    private String area_intro;
    private String area_picture;

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }

    public String getArea_name() {
        return area_name;
    }

    public void setArea_name(String area_name) {
        this.area_name = area_name;
    }

    public String getTourist_route() {
        return tourist_route;
    }

    public void setTourist_route(String tourist_route) {
        this.tourist_route = tourist_route;
    }

    public String getArea_intro() {
        return area_intro;
    }

    public void setArea_intro(String area_intro) {
        this.area_intro = area_intro;
    }

    public String getArea_picture() {
        return area_picture;
    }

    public void setArea_picture(String area_picture) {
        this.area_picture = area_picture;
    }
}
