package pojo;

public class Scenery {
    private int scenery_id;
    private String scenery_name;
    private String scenery_address;
    private String navigation_route;
    private String scenery_picture;
    private String scenery_intro;
    private String scenery_cover;
    private int browse_num;
    private String comment;
    private int area_id;
    private float scenery_cost;

    public int getScenery_id() {
        return scenery_id;
    }

    public void setScenery_id(int scenery_id) {
        this.scenery_id = scenery_id;
    }

    public String getScenery_name() {
        return scenery_name;
    }

    public void setScenery_name(String scenery_name) {
        this.scenery_name = scenery_name;
    }

    public String getScenery_address() {
        return scenery_address;
    }

    public void setScenery_address(String scenery_address) {
        this.scenery_address = scenery_address;
    }

    public String getNavigation_route() {
        return navigation_route;
    }

    public void setNavigation_route(String navigation_route) {
        this.navigation_route = navigation_route;
    }

    public String getScenery_picture() {
        return scenery_picture;
    }

    public void setScenery_picture(String scenery_picture) {
        this.scenery_picture = scenery_picture;
    }

    public String getScenery_intro() {
        return scenery_intro;
    }

    public void setScenery_intro(String scenery_intro) {
        this.scenery_intro = scenery_intro;
    }

    public String getScenery_cover() {
        return scenery_cover;
    }

    public void setScenery_cover(String scenery_cover) {
        this.scenery_cover = scenery_cover;
    }

    public int getBrowse_num() {
        return browse_num;
    }

    public void setBrowse_num(int browse_num) {
        this.browse_num = browse_num;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }

    public float getScenery_cost() {
        return scenery_cost;
    }

    public void setScenery_cost(float scenery_cost) {
        this.scenery_cost = scenery_cost;
    }
}
