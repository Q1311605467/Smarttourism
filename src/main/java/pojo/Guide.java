package pojo;

public class Guide {
    private int guide_id;
    private String guide_name;
    private String guide_head;
    private String guide_intro;
    private String guide_service;
    private String guide_comment;
    private float guide_cost;
    private int scenery_id;
    private String tel;

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }


    public int getGuide_id() {
        return guide_id;
    }

    public void setGuide_id(int guide_id) {
        this.guide_id = guide_id;
    }

    public String getGuide_name() {
        return guide_name;
    }

    public void setGuide_name(String guide_name) {
        this.guide_name = guide_name;
    }

    public String getGuide_head() {
        return guide_head;
    }

    public void setGuide_head(String guide_head) {
        this.guide_head = guide_head;
    }

    public String getGuide_intro() {
        return guide_intro;
    }

    public void setGuide_intro(String guide_intro) {
        this.guide_intro = guide_intro;
    }

    public String getGuide_service() {
        return guide_service;
    }

    public void setGuide_service(String guide_service) {
        this.guide_service = guide_service;
    }

    public String getGuide_comment() {
        return guide_comment;
    }

    public void setGuide_comment(String guide_comment) {
        this.guide_comment = guide_comment;
    }

    public float getGuide_cost() {
        return guide_cost;
    }

    public void setGuide_cost(float guide_cost) {
        this.guide_cost = guide_cost;
    }

    public int getScenery_id() {
        return scenery_id;
    }

    public void setScenery_id(int scenery_id) {
        this.scenery_id = scenery_id;
    }
}
