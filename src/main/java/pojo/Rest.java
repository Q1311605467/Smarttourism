package pojo;

public class Rest {
    private int rest_id;
    private String rest_name;
    private String rest_address;
    private String rest_tel;
    private String rest_cover;
    private String rest_intro;
    private float consumption;
    private String rest_href;
    private int area_id;

    public int getRest_id() {
        return rest_id;
    }

    public void setRest_id(int rest_id) {
        this.rest_id = rest_id;
    }

    public String getRest_name() {
        return rest_name;
    }

    public void setRest_name(String rest_name) {
        this.rest_name = rest_name;
    }

    public String getRest_address() {
        return rest_address;
    }

    public void setRest_address(String rest_address) {
        this.rest_address = rest_address;
    }

    public String getRest_tel() {
        return rest_tel;
    }

    public void setRest_tel(String rest_tel) {
        this.rest_tel = rest_tel;
    }

    public String getRest_cover() {
        return rest_cover;
    }

    public void setRest_cover(String rest_cover) {
        this.rest_cover = rest_cover;
    }

    public String getRest_intro() {
        return rest_intro;
    }

    public void setRest_intro(String rest_intro) {
        this.rest_intro = rest_intro;
    }

    public float getConsumption() {
        return consumption;
    }

    public void setConsumption(float consumption) {
        this.consumption = consumption;
    }

    public String getRest_href() {
        return rest_href;
    }

    public void setRest_href(String rest_href) {
        this.rest_href = rest_href;
    }

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }
}
