package pojo;

public class Hotel {
    private int hotel_id;
    private String hotel_name;
    private String hotel_address;
    private String hotel_tel;
    private String hotel_cover;
    private String accommodation;
    private float hotel_price;
    private String hotel_href;
    private int area_id;


    public int getHotel_id() {
        return hotel_id;
    }

    public void setHotel_id(int hotel_id) {
        this.hotel_id = hotel_id;
    }

    public String getHotel_name() {
        return hotel_name;
    }

    public void setHotel_name(String hotel_name) {
        this.hotel_name = hotel_name;
    }

    public String getHotel_address() {
        return hotel_address;
    }

    public void setHotel_address(String hotel_address) {
        this.hotel_address = hotel_address;
    }

    public String getHotel_tel() {
        return hotel_tel;
    }

    public void setHotel_tel(String hotel_tel) {
        this.hotel_tel = hotel_tel;
    }

    public String getHotel_cover() {
        return hotel_cover;
    }

    public void setHotel_cover(String hotel_cover) {
        this.hotel_cover = hotel_cover;
    }

    public String getAccommodation() {
        return accommodation;
    }

    public void setAccommodation(String accommodation) {
        this.accommodation = accommodation;
    }

    public float getHotel_price() {
        return hotel_price;
    }

    public void setHotel_price(float hotel_price) {
        this.hotel_price = hotel_price;
    }

    public String getHotel_href() {
        return hotel_href;
    }

    public void setHotel_href(String hotel_href) {
        this.hotel_href = hotel_href;
    }

    public int getArea_id() {
        return area_id;
    }

    public void setArea_id(int area_id) {
        this.area_id = area_id;
    }

}
